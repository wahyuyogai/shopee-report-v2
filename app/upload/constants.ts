// Helper to generate unique keys for duplicate detection
export const generateUniqueKey = (row: any, type: 'failed' | 'return' | 'cancelled' | 'order-all' | 'income' | 'my-balance' | 'adwords-bill'): string => {
  const normalize = (val: any) => String(val || '').trim().toLowerCase();
  
  if (type === 'failed') {
    return [
      normalize(row['No. Pesanan']),
      normalize(row['Waktu Pesanan Dibuat']),
      normalize(row['Nama Produk']),
      normalize(row['Nomor Referensi SKU']),
      normalize(row['Nama Variasi']),
      normalize(row['Jumlah']),
      normalize(row['Username (Pembeli)'])
    ].join('|');
  } else if (type === 'return') {
    return [
      normalize(row['No. Pengembalian']),
      normalize(row['No. Pesanan']),
      normalize(row['Tanggal Pesanan Dibuat']),
      normalize(row['Nama Produk']),
      normalize(row['Variasi']),
      normalize(row['Kode Variasi']),
      normalize(row['Jumlah Produk Dikembalikan']),
      normalize(row['Alamat Pengiriman']),
      normalize(row['No. Telepon']),
      normalize(row['No. Resi Pengiriman Terusan'])
    ].join('|');
  } else if (type === 'cancelled') {
    return [
      normalize(row['No. Pesanan']),
      normalize(row['Waktu Pesanan Dibuat']),
      normalize(row['Nama Produk']),
      normalize(row['Nomor Referensi SKU']),
      normalize(row['Nama Variasi']),
      normalize(row['Jumlah']),
      normalize(row['Alasan Pembatalan']),
      normalize(row['Username (Pembeli)'])
    ].join('|');
  } else if (type === 'income') {
    return [
      normalize(row['No. Pesanan']),
      normalize(row['Waktu Pesanan Dibuat']),
      normalize(row['Username (Pembeli)']),
      normalize(row['Tanggal Dana Dilepaskan']),
      normalize(row['Total Penghasilan'])
    ].join('|');
  } else if (type === 'my-balance') {
    // MyBalance often has rows without Order ID (withdrawals), so we use other fields
    return [
      normalize(row['Tanggal Transaksi']),
      normalize(row['Deskripsi']),
      normalize(row['Jumlah']),
      normalize(row['Saldo Akhir'])
    ].join('|');
  } else if (type === 'adwords-bill') {
    return [
      normalize(row['Urutan']),
      normalize(row['Waktu']),
      normalize(row['Deskripsi']),
      normalize(row['Jumlah'])
    ].join('|');
  } else {
    // Order All
    return [
      normalize(row['No. Pesanan']),
      normalize(row['Waktu Pesanan Dibuat']),
      normalize(row['Nama Produk']),
      normalize(row['Nomor Referensi SKU']),
      normalize(row['Nama Variasi']),
      normalize(row['Jumlah']),
      normalize(row['Username (Pembeli)'])
    ].join('|');
  }
};
